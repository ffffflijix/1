/*
    难度的选择，进入界面只有选择返回才能return，回到开始界面
    选择难度后，退出内层循环，打印选中打勾的勾勾（gougou）
*/
void nanduchoose()
{
    // i，j是难度选项选中打勾的打印坐标
    int i = 0, j = 0, m = 0, n = 0;
    //难度界面
    Draw_bmp(0, 0, &nandu);
    while (1)
    {
        //补充选中勾勾处
        Draw_bird(i, j, 30, 30, &nandu);
        switch (sudu)
        {
        case 10:
            i = 346;
            j = 74;
            break;
        case 20:
            i = 565;
            j = 74;
            break;
        case 50:
            i = 481;
            j = 211;
        }
        //打印新勾勾
        Draw_bmp(i, j, &gougou);
        Draw_bird(m, n, 30, 30, &nandu);
        if (P == &back)
        {
            m = 300;
            n = 275;
        }
        else if (P == &back1)
        {
            m = 300;
            n = 365;
        }
        //打印新勾勾
        Draw_bmp(m, n, &gougou);

        //选择难度、背景或退出
        while (1)
        {
            if (p.x > 393 * 1024 / 800 && p.x < 528 * 1024 / 800 && p.y > 61 * 600 / 480 && p.y < 103 * 600 / 480)
            {
                sudu = 10; //低难度，懒得等
                break;
            }
            else if (p.x > 602 * 1024 / 800 && p.x < 755 * 1024 / 800 && p.y > 60 * 600 / 480 && p.y < 103 * 600 / 480)
            {
                sudu = 20; //中难度，速度适中，建议难度
                break;
            }
            else if (p.x > 455 * 1024 / 800 && p.x < 647 * 1024 / 800 && p.y > 145 * 600 / 480 && p.y < 194 * 600 / 480)
            {
                sudu = 50; //随便搞的难度
                break;
            }
            else if (p.x > 652 * 1024 / 800 && p.x < 778 * 1024 / 800 && p.y > 375 * 600 / 480 && p.y < 434 * 600 / 480)
            {
                return; //结束函数，回到开始界面
            }
            else if (p.x > 342 * 1024 / 800 && p.x < 506 * 1024 / 800 && p.y > 274 * 600 / 480 && p.y < 336 * 600 / 480)
            {
                P = &back; //背景1
                break;
            }
            else if (p.x > 347 * 1024 / 800 && p.x < 507 * 1024 / 800 && p.y > 365 * 600 / 480 && p.y < 429 * 600 / 480)
            {
                P = &back1; //背景2
                break;
            }
        }
    }
}

/*
    开始界面，只有开始和难度设置两个选项
*/
void start()
{
    Draw_bmp(0, 0, &kaishi);
    while (1)
    {
        if (p.x > 292 * 1024 / 800 && p.x < 500 * 1024 / 800 && p.y > 286 * 600 / 480 && p.y < 339 * 600 / 480)
        {
            return;
        }
        else if (p.x > 682 * 1024 / 800 && p.x < 800 * 1024 / 800 && p.y > 410 * 600 / 480 && p.y < 480 * 600 / 480)
        {
            nanduchoose();
            //难度选择完毕后，覆盖界面
            Draw_bmp(0, 0, &kaishi);
            usleep(100000);
            continue;
        }
    }
}

/*结束界面：返回值1是重新开始*/
int endless()
{
    Draw_bmp(0, 0, &end);
    scoreprint();
    while (1)
    {
        if (p.x > 323 * 1024 / 800 && p.x < 432 * 1024 / 800 && p.y > 325 * 600 / 480 && p.y < 367 * 600 / 480)
        {
            return 0;
        }
        if (p.x > 281 * 1024 / 800 && p.x < 506 * 1024 / 800 && p.y > 226 * 600 / 480 && p.y < 277 * 600 / 480)
        {
            return 1;
        }
    }
}

